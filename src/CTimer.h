//======================================================================================================================
#include <SDL2/SDL_timer.h>
//======================================================================================================================

//================================class CTimer==========================================================================
class CTimer {
public:
    /**
     * Initialize timer
     */
    CTimer ();

    //------------------------------------------------------------------------------------------------------------------
    ~CTimer ();

    //------------------------------------------------------------------------------------------------------------------
    /**
     * Reset timer.
     */
    void reset ();

    //------------------------------------------------------------------------------------------------------------------
    /**
     * Set timer.
     * @param time
     */
    void setTimer (long int time);

    //------------------------------------------------------------------------------------------------------------------
    /**
     * Elapsed time after initialization of the timer.
     * @return
     */
    long int getTime () const;

private:
    long int m_lastTime;
};
