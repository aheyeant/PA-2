/**
 * @file Implementation of class CTimer.
 */
//======================================================================================================================
#include "CTimer.h"
//======================================================================================================================

//================================class CTimer==========================================================================
/**
 * Initialize timer.
 */
CTimer::CTimer ():m_lastTime(SDL_GetTicks()) {}

//----------------------------------------------------------------------------------------------------------------------
CTimer::~CTimer () = default;

//----------------------------------------------------------------------------------------------------------------------
/**
 * Reset timer.
 */
void CTimer::reset () {
    m_lastTime = SDL_GetTicks();
}

//----------------------------------------------------------------------------------------------------------------------
/**
 * Set timer.
 * @param time
 */
void CTimer::setTimer (long int time) {
    reset();
    while (getTime() - time < 0) {}
}

//----------------------------------------------------------------------------------------------------------------------
/**
 * Elapsed time after initialization of the timer.
 * @return
 */
long int CTimer::getTime () const {
    return SDL_GetTicks() - m_lastTime;
}
